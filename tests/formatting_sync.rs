#[test]
fn sync_trace() {
	let mem_sink = sink::memory::Memory::new(sink::memory::MemoryConfig {
		mock_time: true,
		mock_trace: true,
		..sink::memory::MemoryConfig::default()
	});
	let mem_sink_output = mem_sink.output();

	{
		let mut log = rasant::Logger::new();
		log.set_level(Level::Trace).add_sink(mem_sink);

		log.info("root test info").warn("root test warn").debug("root test debug");

		let mut nlog = log.clone();
		nlog.set("number", 1);
		nlog.info("first test info")
			.warn("first test warn")
			.debug("first test debug")
			.error(Error::new(ErrorKind::NotFound, "oh no"), "something failed");
	}

	let got = mem_sink_output.as_string();
	let want = "2026-03-04 15:10:15.000 [TRA] added new log sink name=\"default log string\" total=1 async=false logger_id=100 caller_filename=\"src/some_file.rs\" caller_line=567\n\
	            2026-03-04 15:10:16.234 [INF] root test info\n\
				2026-03-04 15:10:17.468 [WRN] root test warn\n\
				2026-03-04 15:10:18.702 [DBG] root test debug\n\
				2026-03-04 15:10:19.936 [INF] first test info number=1\n\
				2026-03-04 15:10:21.170 [WRN] first test warn number=1\n\
				2026-03-04 15:10:22.404 [DBG] first test debug number=1\n\
				2026-03-04 15:10:23.638 [ERR] something failed error=\"oh no\" number=1";

	assert_eq!(got, want);
}
